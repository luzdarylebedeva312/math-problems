fn main() {
    // Example: Addition of two numbers
    let num1 = 5;
    let num2 = 3;
    println!("The sum of {} and {} is {}", num1, num2, num1 + num2);
    
    // Example: Division with remainder
    let dividend = 7;
    let divisor = 2;
    println!("The result of dividing {} by {} is {}.", dividend, divisor, dividend / divisor);
}
